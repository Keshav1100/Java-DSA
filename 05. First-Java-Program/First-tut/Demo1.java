// class - named group of properties and functions.
// main - function name , has to be main only.
// static - bcoz we want to run this main function without creating an instance of object Main class
// void - means no return value
// String[] args -  arguments or command line arguments
// String[] - collection of strings of arguments we pass in cmd while running the byte code file
